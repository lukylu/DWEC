//Procedemos a crear el código para la tabla del 7 con el bucle for
for (a = 1; a <= 10; a++) {
    document.write("7 x " + a + " = " + (7*a) + "<br>");
}
document.write("<br>");

//Procedemos a crear el código para la tabla del 8 con el buccle do while
b = 1;
do {
    document.write("8 x " + b + " = " + (8*b) + "<br>");
    b++;
} while (b <= 10);
document.write("<br>");

//Procedemos a crear el código para la tabla del 9 con el bucle while
